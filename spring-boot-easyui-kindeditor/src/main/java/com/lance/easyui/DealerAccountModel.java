package com.lance.easyui;

public class DealerAccountModel {
	private int id;
	
	private String accountCode;
	private String accountName;
	
	private String accountPhone;
	private String bandCardNo;
	private String vcode;
	
	public DealerAccountModel() {
		super();
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAccountCode() {
		return accountCode;
	}
	public void setAccountCode(String accountCode) {
		this.accountCode = accountCode;
	}
	public String getAccountName() {
		return accountName;
	}
	public void setAccountName(String accountName) {
		this.accountName = accountName;
	}
	public String getAccountPhone() {
		return accountPhone;
	}
	public void setAccountPhone(String accountPhone) {
		this.accountPhone = accountPhone;
	}
	public String getBandCardNo() {
		return bandCardNo;
	}
	public void setBandCardNo(String bandCardNo) {
		this.bandCardNo = bandCardNo;
	}
	public String getVcode() {
		return vcode;
	}
	public void setVcode(String vcode) {
		this.vcode = vcode;
	}  
	
	
	 
	
	
}
